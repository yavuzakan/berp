                public void ara()
    {
     
        DefaultTableModel tablo = (DefaultTableModel) jTable1.getModel();         
        int rows = tablo.getRowCount(); 
        for(int i = rows - 1; i >=0; i--)
        {
           tablo.removeRow(i); 
        }
   
        String ara = jTextField1.getText();
        String ara2 = jTextField2.getText();
        String ara3 = jTextField3.getText();



  
   try {
            Class.forName("org.sqlite.JDBC");
            c = DriverManager.getConnection("jdbc:sqlite:berp.sqlite");
            st = c.createStatement();
            
             String query = "SELECT * FROM urun1 where uretimno like '%"+ ara +"%' and anaurun like '%"+ ara2 +"%' and urun like '%"+ ara3 +"%' and durum like 'Üretimde' ";
      
            // System.out.print(query);
        rs = st.executeQuery(query);
       
               while(rs.next())
         {
             
            String id  = rs.getString("uretimno"); 
            String urun = rs.getString("anaurun"); 
            String bolum = rs.getString("urun"); 
            String tur = rs.getString("tarih2"); 
  
           
          //yarım bıraktın devam et.
        tablo.addRow(new Object[] { id, urun , bolum , tur  });

         
         }


       }
       catch ( Exception e ) {
      System.err.println( e.getClass().getName() + ": " + e.getMessage() );
      System.exit(0);
    }
        
        
        
    }
        
 

